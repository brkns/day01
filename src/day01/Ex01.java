package day01;

public class Ex01 {
	public static void main(String[] args) {
		System.out.println("안녕하세요");
		/*
		 * \n 엔터
		 * \t tab크기만큼 오른쪽으로 이동
		 * \" 큰따옴표 표현
		 * \\ 역슬래시 표현
		*/
		System.out.println("안\n녕\n하\n세\n요");
		System.out.println("1\t123\t1234567\t11");
		System.out.println("  \"  \\ ");
	}
}

